package com.corejava.misc;

public class MathClassDemo {

	public static void main(String[] args) {
		System.out.println(Math.abs(14 - 17));
		System.out.println(Math.sqrt(25));
		System.out.println(Math.pow(2, 5));
		System.out.println(Math.round(13.67));
		System.out.println(Math.round(13.47));
		System.out.println(Math.floor(13.67));
		System.out.println(Math.ceil(13.67));
	}

}
